package mypack.beans;

public class Location {
	private String street;

	
	public Location() {
		super();
	}

	public Location(String street) {
		super();
		this.street = street;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}
	
}
